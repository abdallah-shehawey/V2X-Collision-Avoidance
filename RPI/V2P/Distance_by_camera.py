def start_camera():
    """تشغيل الكاميرا وعرض الفيديو المباشر"""

    # كود JavaScript لفتح الكاميرا في Colab
    js = Javascript('''
        async function startCamera() {
            const div = document.createElement('div');
            document.body.appendChild(div);

            const video = document.createElement('video');
            video.width = 640;
            video.height = 480;
            div.appendChild(video);

            const stream = await navigator.mediaDevices.getUserMedia({video: true});
            video.srcObject = stream;
            await video.play();

            const canvas = document.createElement('canvas');
            canvas.width = video.videoWidth;
            canvas.height = video.videoHeight;
            div.appendChild(canvas);

            const ctx = canvas.getContext('2d');

            // Capture frame function
            window.captureFrame = function() {
                ctx.drawImage(video, 0, 0, canvas.width, canvas.height);
                return canvas.toDataURL('image/jpeg', 0.8);
            };

            // Stop camera function
            window.stopCamera = function() {
                stream.getTracks().forEach(track => track.stop());
                div.remove();
            };

            // Show video
            video.style.display = 'block';
            canvas.style.display = 'none';

            return true;
        }
    ''')

    display(js)
    eval_js('startCamera()')

    print("\n📸 Camera started!")
    print("Press Ctrl+C to stop")
    print("="*60)

    detected_plates = {}
    frame_count = 0

    try:
        while True:
            # التقاط صورة من الكاميرا
            img_data = eval_js('captureFrame()')

            if img_data:
                # تحويل البيانات إلى صورة
                img_data = img_data.split(',')[1]
                img_bytes = b64decode(img_data)
                img_np = np.frombuffer(img_bytes, dtype=np.uint8)
                frame = cv2.imdecode(img_np, cv2.IMREAD_COLOR)

                if frame is not None:
                    frame_count += 1

                    # معالجة كل 5 إطارات
                    if frame_count % 5 == 0:
                        processed_frame, plates = read_plate_from_frame(frame)

                        # عرض النتائج
                        for plate_info in plates:
                            plate = plate_info['plate']
                            confidence = plate_info['confidence']
                            distance = plate_info['distance']

                            if plate not in detected_plates:
                                detected_plates[plate] = {
                                    'count': 0,
                                    'confidence': confidence,
                                    'distance': distance
                                }

                            detected_plates[plate]['count'] += 1

                            distance_text = f"{distance:.2f}m" if distance != float('inf') else "Unknown"
                            print(f"\n✅ Detected: {plate}")
                            print(f"   🎯 Confidence: {confidence:.0f}%")
                            print(f"   📏 Distance: {distance_text}")
                            print(f"   📊 Count: {detected_plates[plate]['count']}")

                    # عرض الفيديو
                    cv2_imshow(frame)

    except KeyboardInterrupt:
        print("\n\n⏹ Stopping camera...")
        eval_js('stopCamera()')

    # النتائج النهائية
    print("\n" + "="*60)
    print("📊 FINAL RESULTS")
    print("="*60)
    print("   🚗 Plate - Count - Confidence - Distance")
    print("-"*60)

    for plate, info in detected_plates.items():
        distance = info.get('distance', float('inf'))
        dist_str = f"{distance:.2f}m" if distance != float('inf') else "Unknown"
        print(f"   🚗 {plate} - {info['count']} times (Conf: {info['confidence']:.0f}%) - Dist: {dist_str}")

