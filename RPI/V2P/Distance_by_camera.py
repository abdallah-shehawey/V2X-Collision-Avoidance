# دالة قراءة اللوحة من الصورة
# ============================================

def read_plate_from_frame(frame):
    """قراءة اللوحات من الإطار"""
    results = car_detector(frame, verbose=False, conf=0.5)
    plates_found = []

    for box in results[0].boxes:
        if int(box.cls[0]) == 2:  # سيارة
            x1, y1, x2, y2 = map(int, box.xyxy[0])
            car_width = x2 - x1

            if car_width > MIN_CAR_WIDTH:
                # منطقة اللوحة
                plate_y1 = int(y2 - (y2 - y1) * 0.3)
                plate_region = frame[plate_y1:y2, x1:x2]

                if plate_region.size > 0:
                    # تحسين الصورة
                    thresh = preprocess_for_ocr(plate_region)

                    # قراءة اللوحة
                    result = reader.readtext(thresh, detail=0, paragraph=True)
                    if result:
                        cleaned = re.sub(r'[^A-Z0-9]', '', result[0].upper().replace(' ', ''))

                        if len(cleaned) >= 4:
                            confidence = min(95, 60 + len(cleaned) * 5)
                            plate_width_px = x2 - x1
                            distance = calculate_distance(plate_width_px)

                            plates_found.append({
                                'plate': cleaned,
                                'confidence': confidence,
                                'distance': distance,
                                'box': (x1, y1, x2, y2),
                                'plate_box': (x1, plate_y1, x2, y2)
                            })

                            # رسم على الصورة
                            cv2.rectangle(frame, (x1, y1), (x2, y2), GREEN, 2)
                            cv2.rectangle(frame, (x1, plate_y1), (x2, y2), RED, 2)

                            # كتابة النص
                            font = cv2.FONT_HERSHEY_SIMPLEX
                            distance_text = f"{distance:.2f}m" if distance != float('inf') else "Unknown"

                            cv2.putText(frame, f"Plate: {cleaned}", (x1, y1 - 50),
                                       font, 0.6, YELLOW, 2)
                            cv2.putText(frame, f"Conf: {confidence:.0f}%", (x1, y1 - 25),
                                       font, 0.5, CYAN, 2)
                            cv2.putText(frame, f"Dist: {distance_text}", (x1, y1 - 2),
                                       font, 0.5, WHITE, 2)

    return frame, plates_found

